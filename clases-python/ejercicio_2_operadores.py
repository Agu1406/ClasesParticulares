# 1. Operadores básicos
a = 10
b = 3

print("=== OPERADORES ARITMÉTICOS ===")
print(f"Suma: {a} + {b} = {a + b}")
print(f"Resta: {a} - {b} = {a - b}")
print(f"Multiplicación: {a} * {b} = {a * b}")
print(f"División: {a} / {b} = {a / b}")
print(f"División entera: {a} // {b} = {a // b}")
print(f"Módulo: {a} % {b} = {a % b}")
print(f"Potencia: {a} ** {b} = {a ** b}")

# 2. Operadores de comparación
print("\n=== OPERADORES DE COMPARACIÓN ===")
print(f"{a} > {b}: {a > b}")
print(f"{a} < {b}: {a < b}")
print(f"{a} >= {b}: {a >= b}")
print(f"{a} <= {b}: {a <= b}")
print(f"{a} == {b}: {a == b}")
print(f"{a} != {b}: {a != b}")

# 3. Operadores de asignación
print("\n=== OPERADORES DE ASIGNACIÓN ===")
x = 5
print(f"Valor inicial: x = {x}")

x += 2  # x = x + 2
print(f"Después de x += 2: x = {x}")

x -= 1  # x = x - 1
print(f"Después de x -= 1: x = {x}")

x *= 3  # x = x * 3
print(f"Después de x *= 3: x = {x}")

x /= 2  # x = x / 2
print(f"Después de x /= 2: x = {x}")

